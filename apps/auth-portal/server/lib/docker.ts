// ─────────────────────────────────────────────

const MCP_ENV_DIR = "/etc/hermes-mcp";
const SYSTEMD_UNIT_SRC = join(import.meta.dir, "../../../templates/mcp-hermes@.service");
const SYSTEMD_UNIT_DST = "/etc/systemd/system/mcp-hermes@.service";

/**
 * Generate per-profile env file for the MCP systemd service.
 * File: /etc/hermes-mcp/{name}.env
 * Content: MCP_PORT=56xxx
 */
export function generateMcpEnvFiles(store: ProfileStore): { success: number; details: string[] } {
  const details: string[] = [];
  let success = 0;

  // Ensure env directory exists
  try {
    mkdirSync(MCP_ENV_DIR, { recursive: true });
  } catch {
    // may need sudo — handled by deploy script
  }

  for (const profile of store.profiles) {
    const mcpPort = profile.port + 5000;
    const envPath = join(MCP_ENV_DIR, `${profile.name}.env`);
    const content = `# Auto-generated by Hermes IDE Auth Portal\nMCP_PORT=${mcpPort}\nMCP_PROFILE=${profile.name}\n`;

    try {
      writeFileSync(envPath, content);
      details.push(`${profile.name}: env → ${envPath} (port ${mcpPort})`);
      success++;
    } catch (e: any) {
      details.push(`${profile.name}: FAILED — ${e.message}`);
    }
  }

  return { success, details };
}

/**
 * Install systemd unit template and enable services for all profiles.
 * Requires sudo.
 */
export async function deployMcpServices(store: ProfileStore): Promise<{ success: boolean; output: string }> {
  const outputs: string[] = [];

  // Step 1: Copy unit template
  const templateContent = readFileSync(SYSTEMD_UNIT_SRC, "utf-8");
  const tmpPath = "/tmp/mcp-hermes@.service";
  writeFileSync(tmpPath, templateContent);

  const cp = Bun.spawn(["sudo", "-n", "cp", tmpPath, SYSTEMD_UNIT_DST], { stdout: "pipe", stderr: "pipe" });
  await cp.exited;

  // Step 2: Generate env files
  const envResult = generateMcpEnvFiles(store);
  outputs.push(`Env files: ${envResult.success}/${store.profiles.length}`);

  // Step 3: Reload systemd daemon
  const reload = Bun.spawn(["sudo", "-n", "systemctl", "daemon-reload"], { stdout: "pipe", stderr: "pipe" });
  await reload.exited;
  outputs.push("systemd daemon reloaded");

  // Step 4: Enable and restart each profile's service
  for (const profile of store.profiles) {
    const serviceName = `mcp-hermes@${profile.name}.service`;

    const enable = Bun.spawn(["sudo", "-n", "systemctl", "enable", serviceName], { stdout: "pipe", stderr: "pipe" });
    await enable.exited;

    const restart = Bun.spawn(["sudo", "-n", "systemctl", "restart", serviceName], { stdout: "pipe", stderr: "pipe" });
    const restartErr = await new Response(restart.stderr).text();
    const restartExit = await restart.exited;

    if (restartExit === 0) {
      outputs.push(`${profile.name}: service started`);
    } else {
      outputs.push(`${profile.name}: FAILED — ${restartErr}`);
    }
  }

  return { success: true, output: outputs.join("\n") };
}

/**
 * Restart a single profile's MCP service
 */
export async function restartMcpService(profileName: string): Promise<{ success: boolean; output: string }> {
  const serviceName = `mcp-hermes@${profileName}.service`;
  const proc = Bun.spawn(["sudo", "-n", "systemctl", "restart", serviceName], { stdout: "pipe", stderr: "pipe" });
  const err = await new Response(proc.stderr).text();
  const exitCode = await proc.exited;

  return {
    success: exitCode === 0,
    output: exitCode === 0 ? `${serviceName} restarted` : `Failed: ${err}`,
  };
}
