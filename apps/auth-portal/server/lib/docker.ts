/**
 * Generate nginx config with:
 * - ide.app.dev.nusa.work → Auth Portal (port 51000)
 * - ide-{name}.app.dev.nusa.work → code-server container (per profile port)
 *   - location /           → code-server (port 8443 via host-mapped port)
 *   - location /mcp        → MCP server inside extension host (port 51600 via container IP)
 *   - location /health/mcp → MCP health check
 */
export function generateNginxConfig(store: ProfileStore): string {
  const profileServerBlocks = store.profiles
    .map((profile) => {
      const subdomain = `ide-${profile.name}`;
      const mcpHostPort = profile.port + 5000;
      return `
# ${profile.name} (code-server: ${profile.port}, mcp: ${mcpHostPort})
server {
    listen 443 ssl;
    server_name ${subdomain}.${IDE_DOMAIN};

    ssl_certificate ${SSL_CERT};
    ssl_certificate_key ${SSL_KEY};
    include /etc/letsencrypt/options-ssl-nginx.conf;
    ssl_dhparam /etc/letsencrypt/ssl-dhparams.pem;

    client_max_body_size 200M;

    # MCP Server (Extension Host)
    # Hermes Agent connects here: https://${subdomain}.${IDE_DOMAIN}/mcp
    location /mcp {
        proxy_pass http://127.0.0.1:${mcpHostPort}/mcp;
        proxy_http_version 1.1;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
        proxy_connect_timeout 600s;
        proxy_send_timeout 600s;
        proxy_read_timeout 600s;
    }

    # MCP Health check
    location = /health/mcp {
        proxy_pass http://127.0.0.1:${mcpHostPort}/health;
        proxy_http_version 1.1;
        proxy_set_header Host $host;
    }

    # Code-server (default)
    location / {
        proxy_pass http://127.0.0.1:${profile.port}/;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
        proxy_set_header X-Forwarded-Host $host;
        proxy_set_header Accept-Encoding gzip;
        proxy_connect_timeout 7d;
        proxy_send_timeout 7d;
        proxy_read_timeout 7d;
    }
}`;
    })
    .join("\n");

  // Collect all server_names for the HTTP→HTTPS redirect block
  const allSubdomains = store.profiles
    .map((p) => `ide-${p.name}.${IDE_DOMAIN}`)
    .join(" ");

  return `# AUTO-GENERATED by Hermes IDE Auth Portal — DO NOT EDIT MANUALLY
# Generated at: ${new Date().toISOString()}
# Pattern: ide-{profile}.${IDE_DOMAIN}

# ──────────────────────────────────────────────
# HTTP → HTTPS redirect (all IDE subdomains)
# ──────────────────────────────────────────────
server {
    listen 80;
    server_name ${AUTH_PORTAL_DOMAIN} ${allSubdomains};
    return 301 https://$host$request_uri;
}

# ──────────────────────────────────────────────
# Auth Portal: ${AUTH_PORTAL_DOMAIN}
# ──────────────────────────────────────────────
server {
    listen 443 ssl;
    server_name ${AUTH_PORTAL_DOMAIN};

    ssl_certificate ${SSL_CERT};
    ssl_certificate_key ${SSL_KEY};
    include /etc/letsencrypt/options-ssl-nginx.conf;
    ssl_dhparam /etc/letsencrypt/ssl-dhparams.pem;

    client_max_body_size 10M;

    location / {
        proxy_pass http://127.0.0.1:${AUTH_PORTAL_PORT};
        proxy_http_version 1.1;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
    }
}

# ──────────────────────────────────────────────
# Code-server profiles: ide-{name}.app.dev.nusa.work
# ──────────────────────────────────────────────
${profileServerBlocks}
`;
}

export function writeNginxConfig(store: ProfileStore): void {
  const content = generateNginxConfig(store);
  writeFileSync(NGINX_TMP_PATH, content);
}

export async function reloadNginx(): Promise<{ success: boolean; output: string }> {
  // Step 1: Copy from tmp to sites-available
  const cp = Bun.spawn(["sudo", "-n", "cp", NGINX_TMP_PATH, NGINX_SITES_PATH], {
    stdout: "pipe",
    stderr: "pipe",
  });
  await cp.exited;

  // Step 2: Ensure symlink in sites-enabled
  const ln = Bun.spawn(["sudo", "-n", "ln", "-sf", NGINX_SITES_PATH, NGINX_ENABLED_PATH], {
    stdout: "pipe",
    stderr: "pipe",
  });
  await ln.exited;

  // Step 3: Test nginx config
  const test = Bun.spawn(["sudo", "-n", "nginx", "-t"], { stdout: "pipe", stderr: "pipe" });
  const testOut = await new Response(test.stderr).text(); // nginx -t outputs to stderr
  const testExit = await test.exited;

  if (testExit !== 0) {
    return { success: false, output: `nginx config test failed: ${testOut}` };
  }

  // Step 4: Reload nginx
  const reload = Bun.spawn(["sudo", "-n", "systemctl", "reload", "nginx"], {
    stdout: "pipe",
    stderr: "pipe",
  });
  const reloadOut = await new Response(reload.stderr).text();
  const reloadExit = await reload.exited;

  return {
    success: reloadExit === 0,
    output: reloadExit === 0
      ? "Nginx config deployed and reloaded successfully"
      : `Nginx reload failed: ${reloadOut}`,
  };
}

/** Get the HTTPS IDE URL for a profile */
export function getIdeUrl(profileName: string): string {
  return `https://ide-${profileName}.${IDE_DOMAIN}/`;
}

/** Get the MCP URL for a profile (domain-based, no port needed) */
export function getMcpUrl(profileName: string): string {
  return `https://ide-${profileName}.${IDE_DOMAIN}/mcp`;
}

