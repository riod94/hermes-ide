export function generateDockerCompose(store: ProfileStore): string {
  const services: string[] = [];

  for (const profile of store.profiles) {
    const serviceName = `hermes-ide-${profile.name}`;
    const apiPort = profile.port + 10000;
    const hermesApiKey = getHermesApiKey(profile.name, profile.password);
    
    services.push(`  ${serviceName}:
    image: ${CODE_SERVER_IMAGE}
    container_name: ${serviceName}
    environment:
      - PUID=1000
      - PGID=1000
      - TZ=Asia/Jakarta
      - PASSWORD=${profile.password}
      - DEFAULT_WORKSPACE=/projects
      - HERMES_API_URL=http://103.196.116.213:${apiPort}/v1
      - HERMES_API_KEY=${hermesApiKey}
    volumes:
      - /home/ade/projects/${profile.name}:/projects
      - ./config-${profile.name}:/config
      - /home/ade/projects/rio/hermes-ide-extension/apps/extension:/hermes-extension:ro
    ports:
      - "${profile.port}:8443"
    networks:
      - hermes-ide-net
    restart: unless-stopped`);
  }

  return `# AUTO-GENERATED by Auth Portal API — DO NOT EDIT MANUALLY
# Generated at: ${new Date().toISOString()}

networks:
  hermes-ide-net:
    driver: bridge

services:
${services.join("\n\n")}
`;
}

export function writeDockerCompose(store: ProfileStore): void {
  const content = generateDockerCompose(store);
  writeFileSync(DOCKER_COMPOSE_PATH, content);
}

export async function restartContainer(serviceName: string): Promise<{ success: boolean; output: string }> {
  const cwd = join(import.meta.dir, "../../../code-server-infra");
  // using --no-deps to only recreate the specified container
  const upProc = Bun.spawn(["docker", "compose", "up", "-d", "--no-deps", serviceName], { cwd, stdout: "pipe", stderr: "pipe" });
  const upOutput = await new Response(upProc.stdout).text();
  const upErr = await new Response(upProc.stderr).text();
  const exitCode = await upProc.exited;

  return {
    success: exitCode === 0,
    output: [upOutput, upErr].filter(Boolean).join("\n"),
  };
}

export async function restartContainers(): Promise<{ success: boolean; output: string }> {
  const cwd = join(import.meta.dir, "../../../code-server-infra");
  const proc = Bun.spawn(["docker", "compose", "down", "--remove-orphans"], { cwd, stdout: "pipe", stderr: "pipe" });
  const downOutput = await new Response(proc.stdout).text();
  const downErr = await new Response(proc.stderr).text();
  await proc.exited;

  const upProc = Bun.spawn(["docker", "compose", "up", "-d"], { cwd, stdout: "pipe", stderr: "pipe" });
  const upOutput = await new Response(upProc.stdout).text();
  const upErr = await new Response(upProc.stderr).text();
  const exitCode = await upProc.exited;

  return {
    success: exitCode === 0,
    output: [downOutput, downErr, upOutput, upErr].filter(Boolean).join("\n"),
  };
}

/** Install extension on a running container via docker exec */
export async function installExtensionOnContainer(profileName: string): Promise<boolean> {
  const containerName = `hermes-ide-${profileName}`;
  const cwd = join(import.meta.dir, "../../../code-server-infra");
  
  // Extension file inside the container
  // ls -t: pick the newest .vsix in case multiple versions exist
  const script = `
    VSIX=\$(ls -t /hermes-extension/*.vsix | head -n 1)
    if [ -n "\$VSIX" ]; then
      echo "Installing \$VSIX..."
      # Clean up old hermes extensions in BOTH storage locations
      # Location 1: --extensions-dir (primary)
      rm -rf /config/extensions/nusawork.hermes-ide* 2>/dev/null || true
      rm -f /config/extensions/.obsolete 2>/dev/null || true
      # Location 2: shadow copy (code-server LSIO auto-creates this)
      rm -rf /config/.local/share/code-server/extensions/nusawork.hermes-ide* 2>/dev/null || true
      
      # Install using code-server CLI to the explicitly configured dir
      /app/code-server/bin/code-server --extensions-dir /config/extensions --install-extension "\$VSIX" --force
      
      # Sync to shadow copy location so code-server serves the correct version
      cp -r /config/extensions/nusawork.hermes-ide-* /config/.local/share/code-server/extensions/ 2>/dev/null || true
      
      # Ensure permissions for the abc user in both locations
      chown -R abc:abc /config/extensions 2>/dev/null || true
      chown -R abc:abc /config/.local/share/code-server/extensions/nusawork.hermes-ide-* 2>/dev/null || true
      exit 0
    else
      echo "No .vsix found in /hermes-extension/"
      exit 1
    fi
  `;

  // Run as root so we can chown afterwards
  const proc = Bun.spawn(["docker", "exec", containerName, "sh", "-c", script], { cwd, stdout: "pipe", stderr: "pipe" });
  const exitCode = await proc.exited;
  return exitCode === 0;
}

