const SSL_CERT = "/etc/letsencrypt/live/app.dev.nusa.work-0001/fullchain.pem";
const SSL_KEY = "/etc/letsencrypt/live/app.dev.nusa.work-0001/privkey.pem";
const AUTH_PORTAL_PORT = 51000;

const CODE_SERVER_IMAGE = "lscr.io/linuxserver/code-server:latest";
